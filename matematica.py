import math

# Teto
print(math.ceil(10.5))

# Piso
print(math.floor(10.5))

# Fatorial
print(math.factorial(3))

# Potencia
print(math.pow(5, 2))   # Retorna valor float
print(5**2)             # NÃ£o necessariamente retorna float

# Raiz quadrada
print(math.sqrt(4))

# Maior divisor comum (MDC)
print(math.gcd(5, 20))
